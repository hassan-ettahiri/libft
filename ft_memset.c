#include <stdio.h>
#include <unistd.h>
#include <string.h>
#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*ptr;
	size_t	i;

	ptr = (char *)b;
	i = 0;
	while (i < len)
	{
		*ptr = c;
		i++;
		ptr++;
	}
	return (b);
}
